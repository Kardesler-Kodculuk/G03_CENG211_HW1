package geography;

public class City {
	private byte plateNo;
	private String name;
	private Region region;
	private double altitude;
	public byte getPlateNo() {
		return plateNo;
	}
	public String getName() {
		return name;
	}
	public Region getRegion() {
		return region;
	}
	public double getAltitude() {
		return altitude;
	}
}
